package com.github.chicoferreira.goldnation.terrains.database.credentials;

public class DatabaseCredentials {

    private String host;
    private int port;
    private String username;
    private String password;
    private String database;

    private DatabaseCredentials(String host, int port, String username, String password, String database) {
        this.host = host;
        this.port = port;
        this.username = username;
        this.password = password;
        this.database = database;
    }

    public static DatabaseCredentials with(String host, int port, String username, String password, String database) {
        return new DatabaseCredentials(host, port, username, password, database);
    }


    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getDatabase() {
        return database;
    }

    public void setDatabase(String database) {
        this.database = database;
    }
}
